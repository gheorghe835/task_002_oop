package car;

public class Car {
    private String brand;
    private String model;
    private int speedMax;
    private int speedNow;

    public Car(String brand, String model, int speedMax, int speedNow) {
        this.brand = brand;
        this.model = model;
        this.speedMax = speedMax;
        this.speedNow = speedNow;
    }
}
